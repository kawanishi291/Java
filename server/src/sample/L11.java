package sample;

class L11{
    
    public static void main (String args[]){
        int x, y, ans;
        x = 10;
        y = 3;
        ans = x + y;
        System.out.println("X=" + x + ",Y=" + y +" (和) --> " + ans);
        ans = x - y;
        System.out.println("X=" + x + ",Y=" + y +" (差) --> " + ans);
        ans = x * y;
        System.out.println("X=" + x + ",Y=" + y +" (積) --> " + ans);
        ans = x / y;
        System.out.println("X=" + x + ",Y=" + y +" (商) --> " + ans);
        ans = x % y;
        System.out.println("X=" + x + ",Y=" + y +" (剰余) --> " + ans);
    }
    
    
}
